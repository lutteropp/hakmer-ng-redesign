// values taken from: https://www.ncbi.nlm.nih.gov/pmc/articles/PMC3904525/
const int GAP_OPEN_PENALTY = -2;
const int GAP_EXTENSION_PENALTY = -9;

class Blosum62 {
public:
	Blosum62();
	int getSubstitutionPenalty(char a, char b) const;
	int getGapOpenPenalty() const;
	int getGapExtendPenalty() const;
	int getGapPenalty() const;
private:
	std::vector<std::vector<int> > entries;
	std::unordered_map<char, size_t> proteinMapping;
};

int Blosum62::getGapExtendPenalty() const {
	return GAP_EXTENSION_PENALTY;
}

int Blosum62::getGapOpenPenalty() const {
	return GAP_OPEN_PENALTY;
}

int Blosum62::getGapPenalty() const {
	return GAP_OPEN_PENALTY + GAP_EXTENSION_PENALTY;
}

int Blosum62::getSubstitutionPenalty(char a, char b) const {
	return entries[proteinMapping[a]][proteinMapping[b]];
}

